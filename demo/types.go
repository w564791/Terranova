type ValueType int

const (
	TypeInvalid ValueType = iota
	TypeBool
	TypeInt
	TypeFloat
	TypeString
	TypeList
	TypeMap
	TypeSet
	TypeObject
	TypeJsonString
	TypeText
	TypeListObject
)

type Schema struct {
	Type     ValueType `json:"type"`     // 字段类型，例如字符串、整数等
	Required bool      `json:"required"` // 字段是否是必需的
	// Optional              bool        `json:"optional"`                 // 字段是否是可选的
	ForceNew              bool             `json:"force_new"`                // 字段更改时是否需要重建资源,update时直接灰色显示
	DiffSuppressOnRefresh bool             `json:"diff_suppress_on_refresh"` // 刷新时是否抑制差异
	Default               interface{}      `json:"default"`                  // 字段的默认值
	Description           string           `json:"description"`              // 字段的描述信息
	InputDefault          string           `json:"input_default"`            // 输入的默认值
	Elem                  interface{}      `json:"elem"`                     // 元素类型，用于集合类型（如列表、集合）
	MaxItems              int              `json:"max_items"`                // 集合类型的最大元素数量
	MaxValue              int              `json:"max_value"`                // 最大值
	MinItems              int              `json:"min_items"`                // 集合类型的最大元素数量
	MinValue              int              `json:"min_value"`                // 最小值
	ComputedWhen          []string         `json:"computed_when"`            // 当这些字段被设置时，该字段是计算得出的
	ConflictsWith         []string         `json:"conflicts_with"`           // 与这些字段冲突，不能同时设置
	ExactlyOneOf          []string         `json:"exactly_one_of"`           // 这些字段中必须且只能有一个被设置
	AtLeastOneOf          []string         `json:"at_least_one_of"`          // 这些字段中至少有一个必须被设置
	RequiredWith          []string         `json:"required_with"`            // 如果设置了这些字段，则这些字段也必须被设置
	RequiredBy            []string         `json:"required_by"`              // 如果设置了这些字段，则这些字段也必须被设置
	FilterWith            []string         `json:"filter_with"`              // 该字段需要被某些字段过滤.例如当需要值时,该最终结果需要通过filter_with里的值进行过滤
	Deprecated            string           `json:"deprecated"`               // 字段是否已弃用，以及弃用的原因
	Sensitive             bool             `json:"sensitive"`                // 字段是否敏感，例如密码
	WriteOnly             bool             `json:"write_only"`               // 字段是否只写，例如密码
	MustInclude           []string         `json:"must_include"`             // 必须包含的值
	UniqItems             bool             `json:"uniq_items"`               // 集合中的元素是否必须唯一
	Color                 Color            `json:"color"`                    //颜色
	Dynamic               string           `json:"dynamic"`                  //是否动态获取接口
	Alias                 string           `json:"alias"`                    // 别名,用于标识字段的中文含义
	HiddenDefault         bool             `json:"hidden_default"`           // 是否默认隐藏,新建时默认不自动渲染,但是可以通过添加按钮新增参数,对于存量应用,有数据需要渲染,没有数据,不做渲染
	Validation            []ValidationRule `json:"validation,omitempty"`     // 验证规则数组，可选字段，包含多个验证规则
}

// ValidationRule represents a validation rule
type ValidationRule struct {
	Condition    string `json:"condition"`     // 验证条件表达式，如 "${contains([\"irsa\", \"service\"], var.trust_policy_type)}"
	ErrorMessage string `json:"error_message"` // 验证失败时的错误消息
}
type Color string

const (
	// WarningColor 代表警告样式（红色）
	WarningColor Color = "color: rgba(245, 53, 5, 0.77); cursor: pointer;"

	// InfoColor 代表提示样式（灰色）
	InfoColor Color = "color: rgba(153, 153, 153, 0.94); cursor: pointer;"
)
